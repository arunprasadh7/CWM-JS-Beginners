// switch case

// let role = "guest";
let role = "moderator";

switch (role) {
  case "guest":
    console.log("Guest user");
    break;

  case "moderator":
    console.log("Moderator");
    break;

  default:
    console.log("Unknown user");
}

// days of week

let day = 1;

switch (day) {
  case 1:
    console.log("Sunday");
    break;

  case 2:
    console.log("Monday");
    break;

  case 3:
    console.log("Tuesday");
    break;

  case 4:
    console.log("Wednesday");
    break;

  case 5:
    console.log("Thursday");
    break;

  case 6:
    console.log("Friday");
    break;

  case 7:
    console.log("Saturday");
    break;

  default:
    console.log("Enter a number between 1 and 7");
}
